    private void cargarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) tblTelefonosVeterinario.getModel();
        modeloTabla.setRowCount(0);
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rmsd;
        int columnas;
        
        int[] anchos = {50, 50};
        for(int i = 0; i < tblTelefonosVeterinario.getColumnCount();i++){
            tblTelefonosVeterinario.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        String user = IDVeterinario.getText();
        try{
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT IDVeterinario, Telefono FROM TelefonoVeterinario WHERE IDVeterinario = ?");
            ps.setString(1, user);
            rs = ps.executeQuery();
            rmsd = rs.getMetaData();
            columnas = rmsd.getColumnCount();
            
            while(rs.next()){
                Object[] fila = new Object[columnas];
                for(int indice = 0; indice < columnas; indice++){
                    fila[indice] = rs.getObject(indice+1);
                }
                modeloTabla.addRow(fila);
                
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    
