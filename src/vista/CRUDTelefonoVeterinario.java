    private void tblTelefonosVeterinarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTelefonosVeterinarioMouseClicked
        try{
            PreparedStatement ps;
            ResultSet rs;
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT IDVeterinario, Telefono FROM TelefonoVeterinario");
            rs = ps.executeQuery();
            while(rs.next()){
                IDVeterinario.setText(rs.getString("IDVeterinario"));
                Telefono.setText(rs.getString("Telefono"));
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_tblTelefonosVeterinarioMouseClicked

