    private void tblCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoriaMouseClicked
        try{
            int fila = tblCategoria.getSelectedRow();
            int id = Integer.parseInt(tblCategoria.getValueAt(fila, 0).toString());
            PreparedStatement ps;
            ResultSet rs;
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT IDCategoria, NombreCategoria, Descripcion, Estado FROM CategoriaServicio_Procedimiento WHERE IDCategoria=?");
            ps.setInt(1, id);
            rs = ps.executeQuery();
            while(rs.next()){
                txtId.setText(String.valueOf(id));
                txtNombreCategoria.setText(rs.getString("NombreCategoria"));
                txtDescripcionCategori.setText(rs.getString("Descripcion"));
                cboEstado.setSelectedItem(rs.getString("Estado"));              
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_tblCategoriaMouseClicked

