    private void cargarTablaGeneral(){
        DefaultTableModel modeloTabla = (DefaultTableModel) tblCitasGeneralesCliente.getModel();
        modeloTabla.setRowCount(0);
        
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rmsd;
        int columnas;
        
        int[] anchos = {25, 150, 100, 150, 100, 100, 100, 50, 50};
        for(int i = 0; i < tblCitasGeneralesCliente.getColumnCount();i++){
            tblCitasGeneralesCliente.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        //String user = estado;
        try{
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT IDCita, Fecha, Domicilio, NombreUsuario, IDProcedimiento_Servicio, Estado, numFactura, CedulaCliente, usuarioCliente FROM CitasMedicas");
            //ps.setString(1, user);
            rs = ps.executeQuery();
            rmsd = rs.getMetaData();
            columnas = rmsd.getColumnCount();
            
            while(rs.next()){
                Object[] fila = new Object[columnas];
                for(int indice = 0; indice < columnas; indice++){
                    fila[indice] = rs.getObject(indice+1);
                }
                modeloTabla.addRow(fila);
                
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }
    
