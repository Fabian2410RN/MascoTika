    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        try{
            int fila = tblClientes.getSelectedRow();
            int id = Integer.parseInt(tblClientes.getValueAt(fila, 0).toString());
            PreparedStatement ps;
            ResultSet rs;
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT CedulaCliente, NombreCliente, Provincia, Canton, Distrito, Señas, Correo, UGoogleMaps FROM Cliente WHERE CedulaCliente=?");
            ps.setInt(1, id);
            rs = ps.executeQuery();
            while(rs.next()){
                CedulaCliente.setText(String.valueOf(id));
                NombreCliente.setText(rs.getString("NombreCliente"));
                //Contraseña.setText(rs.getString("Contraseña"));
                Provincia.setText(rs.getString("Provincia"));
                Canton.setText(rs.getString("Canton"));
                Distrito.setText(rs.getString("Distrito"));
                Señas.setText(rs.getString("Señas"));
                //txtNombreUsuario.setText(rs.getString("NombreUsuario"));
                Correo.setText(rs.getString("Correo"));
                UGoogleMaps.setText(rs.getString("UGoogleMaps"));   
                          
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_tblClientesMouseClicked

